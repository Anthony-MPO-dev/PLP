-- Funcoes

soma x y = x + y 

inc n = n + 1

areaTriagulo b h = (b * h) / 2


areaCirculo r = pi * r * r

areaTrapezio a b h = (a + b) * h / 2

dobraValor a = a * 2

quadruplicaValor a = (dobraValor a) * 2 

distanciaP1P2 (x,y) (x2, y2) = sqrt(x2 - x) + sqrt(y2 - y)

hipotenusa c1 c2 = sqrt(c1*c1 + c2*c2)